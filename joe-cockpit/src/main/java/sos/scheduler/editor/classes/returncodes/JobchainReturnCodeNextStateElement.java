package sos.scheduler.editor.classes.returncodes;

public class JobchainReturnCodeNextStateElement {

    private String returnCodes;
    private String nextState;

    public JobchainReturnCodeNextStateElement() {
        super();
    }

    public String getReturnCodes() {
        return returnCodes;
    }

    public void setReturnCodes(String returnCodes_) {
        this.returnCodes = returnCodes_;
    }

    public String getNextState() {
        return nextState;
    }

    public void setNextState(String nextState) {
        this.nextState = nextState;
    }

}
